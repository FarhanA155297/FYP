$('#field1').on('change', function() {
    if(this.value == "Name") {
      $('#field1-name').css('display', 'block');
      $('#field1-shape').css('display', 'none');
      $('#field1-brand').css('display', 'none');
      $('#field1-price').css('display', 'none');
      $('#field1-color').css('display', 'none');
    } else if(this.value == "Shape") {
      $('#field1-name').css('display', 'none');
      $('#field1-shape').css('display', 'block');
      $('#field1-brand').css('display', 'none');
      $('#field1-price').css('display', 'none');
      $('#field1-color').css('display', 'none');
    } else if(this.value == "Brand") {
      $('#field1-name').css('display', 'none');
      $('#field1-shape').css('display', 'none');
      $('#field1-brand').css('display', 'block');
      $('#field1-price').css('display', 'none');
      $('#field1-color').css('display', 'none');
    } else if(this.value == "Price") {
      $('#field1-name').css('display', 'none');
      $('#field1-shape').css('display', 'none');
      $('#field1-brand').css('display', 'none');
      $('#field1-price').css('display', 'block');
      $('#field1-color').css('display', 'none');
    } else if(this.value == "Color") {
      $('#field1-name').css('display', 'none');
      $('#field1-shape').css('display', 'none');
      $('#field1-brand').css('display', 'none');
      $('#field1-price').css('display', 'none');
      $('#field1-color').css('display', 'block');
    }
});

